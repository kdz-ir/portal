/**
 * Enum for age types with values representing different age groups.
 */
export enum AgeType {
    /**
     * Represents individuals who are 18 years old or older.
     */
    plus18 = 1,
    /**
     * Represents individuals who are between 13 and 18 years old.
     */
    between13And18 = 2,
    /**
     * Represents individuals who are under 13 years old.
     */
    under13 = 3
}
